    private boolean confereSenha(String login,String senha){
        Usuario usuario = ConexaoBancoDados.UsuarioDAO.carregaPorLogin(login);
        if (usuario.getSenha().equals(senha)){
            return true;
        }else{
        return false;
    }
    }
    
    
